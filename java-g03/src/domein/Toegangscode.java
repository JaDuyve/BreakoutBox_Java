package domein;

public class Toegangscode {
    private int id;
    private int code;

    public Toegangscode(int id, int code){
        this.id = id;
        this.code = code;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }
}
